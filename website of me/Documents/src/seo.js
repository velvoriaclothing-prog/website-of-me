function ensureAbsoluteBaseUrl(baseUrl) {
  const value = String(baseUrl || "").trim();
  if (!value) return "https://example.com";
  return value.replace(/\/+$/, "");
}

function getSeoForPath(pathname) {
  return pageSeo[pathname] || null;
}

function buildCanonicalUrl(baseUrl, canonicalPath) {
  return `${ensureAbsoluteBaseUrl(baseUrl)}${canonicalPath === "/" ? "/" : canonicalPath}`;
}

function injectSeo(html, seo, baseUrl) {
  if (!seo) return html;

  const canonicalUrl = buildCanonicalUrl(baseUrl, seo.canonicalPath || "/");
  const seoBlock = [
    `  <meta name="robots" content="${seo.robots || "index,follow"}">`,
    `  <meta property="og:title" content="${seo.ogTitle || seo.title}">`,
    `  <meta property="og:description" content="${seo.ogDescription || seo.description}">`,
    `  <meta property="og:type" content="${seo.ogType || "website"}">`,
    `  <meta property="og:url" content="${canonicalUrl}">`,
    `  <meta name="twitter:card" content="summary_large_image">`,
    `  <link rel="canonical" href="${canonicalUrl}">`
  ].join("\n");

  return html
    .replace(/<title>[\s\S]*?<\/title>/i, `<title>${seo.title}</title>`)
    .replace(/<meta name="description" content="[^"]*">/i, `<meta name="description" content="${seo.description}">`)
    .replace(/<\/head>/i, `${seoBlock}\n</head>`);
}

function buildRobotsTxt(baseUrl) {
  return `User-agent: *\nAllow: /\nSitemap: ${ensureAbsoluteBaseUrl(baseUrl)}/sitemap.xml\n`;
}

function buildSitemapXml(baseUrl) {
  const root = ensureAbsoluteBaseUrl(baseUrl);
  const urls = Object.values(pageSeo)
    .filter((page) => page.robots !== "noindex,nofollow" && page.robots !== "noindex,follow")
    .map((page) => buildCanonicalUrl(root, page.canonicalPath || "/"));

  return [
    '<?xml version="1.0" encoding="UTF-8"?>',
    '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
    ...urls.map((url) => `  <url><loc>${url}</loc></url>`),
    "</urlset>"
  ].join("\n");
}

