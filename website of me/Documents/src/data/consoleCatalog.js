function slugify(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function buildConsoleEntries(platform, names) {
  const pool = consoleImagePools[platform] || consoleImagePools.PS5;
  const seedOffset = platform === "PS5" ? 5000 : 7000;
  return names.map((name, index) => ({
    id: `${platform.toLowerCase()}-seed-${String(index + 1).padStart(3, "0")}`,
    slug: slugify(`${platform}-${name}`),
    name,
    platform,
    image: `${pool[index % pool.length]}&sig=${seedOffset + index + 1}`
  }));
}

